"""
Population-level diversity metrics for aptamer sets.
Used to evaluate the diversity of sequences generated by in-silico algorithms.
"""

from __future__ import annotations
import numpy as np
from aptamer_bench.sequence import AptamerSequence


class DiversityMetrics:
    """
    Compute diversity metrics across a population of aptamer sequences.

    These metrics are critical for benchmarking in-silico generation algorithms:
    a good algorithm should produce diverse, non-redundant sequences.

    Usage
    -----
    >>> from aptamer_bench import AptamerSequence, DiversityMetrics
    >>> seqs = [AptamerSequence("GCCTGTTGTGAGCC", "DNA"),
    ...         AptamerSequence("ATCGATCGATCGAT", "DNA")]
    >>> dm = DiversityMetrics()
    >>> print(dm.mean_pairwise_distance(seqs))
    """

    def mean_pairwise_distance(
        self, aptamers: list[AptamerSequence], normalize: bool = True
    ) -> float:
        """
        Compute mean pairwise Levenshtein (edit) distance across all sequence pairs.

        Parameters
        ----------
        aptamers : list[AptamerSequence]
        normalize : bool
            If True, normalize by average sequence length.

        Returns
        -------
        float
            Mean pairwise edit distance (normalized if requested).
        """
        seqs = [a.sequence for a in aptamers]
        n = len(seqs)
        if n < 2:
            return 0.0

        distances = []
        for i in range(n):
            for j in range(i + 1, n):
                d = self._levenshtein(seqs[i], seqs[j])
                if normalize:
                    avg_len = (len(seqs[i]) + len(seqs[j])) / 2
                    d = d / avg_len if avg_len > 0 else 0.0
                distances.append(d)

        return float(np.mean(distances))

    def redundancy_rate(self, aptamers: list[AptamerSequence]) -> float:
        """
        Fraction of duplicate sequences in the population.

        Parameters
        ----------
        aptamers : list[AptamerSequence]

        Returns
        -------
        float
            Redundancy rate in [0, 1]. 0 = all unique, 1 = all identical.
        """
        if not aptamers:
            return 0.0
        total = len(aptamers)
        unique = len({a.sequence for a in aptamers})
        return 1.0 - (unique / total)

    def population_gc_stats(self, aptamers: list[AptamerSequence]) -> dict:
        """
        Compute GC content statistics across a population.

        Returns
        -------
        dict with keys: mean, std, min, max
        """
        gc_values = [a.gc_content for a in aptamers]
        return {
            "mean": float(np.mean(gc_values)),
            "std": float(np.std(gc_values)),
            "min": float(np.min(gc_values)),
            "max": float(np.max(gc_values)),
        }

    def length_distribution(self, aptamers: list[AptamerSequence]) -> dict:
        """
        Compute sequence length distribution statistics.

        Returns
        -------
        dict with keys: mean, std, min, max
        """
        lengths = [a.length for a in aptamers]
        return {
            "mean": float(np.mean(lengths)),
            "std": float(np.std(lengths)),
            "min": int(np.min(lengths)),
            "max": int(np.max(lengths)),
        }

    def summary(self, aptamers: list[AptamerSequence]) -> dict:
        """
        Full diversity summary for a population.

        Returns
        -------
        dict
        """
        return {
            "n_sequences": len(aptamers),
            "n_unique": len({a.sequence for a in aptamers}),
            "redundancy_rate": self.redundancy_rate(aptamers),
            "mean_pairwise_distance_normalized": self.mean_pairwise_distance(aptamers),
            "gc_content": self.population_gc_stats(aptamers),
            "length": self.length_distribution(aptamers),
        }

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _levenshtein(s1: str, s2: str) -> int:
        """Compute Levenshtein edit distance between two strings."""
        m, n = len(s1), len(s2)
        # Use a single-row DP approach for memory efficiency
        prev = list(range(n + 1))
        for i in range(1, m + 1):
            curr = [i] + [0] * n
            for j in range(1, n + 1):
                if s1[i - 1] == s2[j - 1]:
                    curr[j] = prev[j - 1]
                else:
                    curr[j] = 1 + min(prev[j], curr[j - 1], prev[j - 1])
            prev = curr
        return prev[n]
